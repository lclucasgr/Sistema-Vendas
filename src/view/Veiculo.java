    public void readJTable() {

        DefaultTableModel modelo = (DefaultTableModel) tbVeiculos.getModel();
        modelo.setNumRows(0);
        DaoVeiculos dv = new DaoVeiculos();

        for (Veiculos v : dv.getVeiculos()) {

            modelo.addRow(new Object[]{
                v.getId(),
                v.getMarca(),
                v.getModelo(),
                v.getAno(),
                v.getPlaca(),
                v.getTipo(),
                v.getUf()
            });
                

        }

    }


