    private void tbVeiculosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbVeiculosMouseClicked
        if (tbVeiculos.getSelectedRow() != -1) {

            edMarca.setText(tbVeiculos.getValueAt(tbVeiculos.getSelectedRow(), 1).toString());
            edModelo.setText(tbVeiculos.getValueAt(tbVeiculos.getSelectedRow(), 2).toString());
            edAno.setText(tbVeiculos.getValueAt(tbVeiculos.getSelectedRow(), 3).toString());
            edPlaca.setText(tbVeiculos.getValueAt(tbVeiculos.getSelectedRow(), 4).toString());
            
        }
    }//GEN-LAST:event_tbVeiculosMouseClicked

