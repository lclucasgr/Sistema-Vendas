    public void readJTable() {

        DefaultTableModel modelo = (DefaultTableModel) tbUsuarios.getModel();
        modelo.setNumRows(0);
        DaoUsuarios du = new DaoUsuarios();

        for (Usuarios u : du.getUsuarios()) {

            modelo.addRow(new Object[]{
                u.getId(),
                u.getNome(),
                u.getLogin(),
                u.getSenha()
            });

        }

    }

    public void readJTableByLogin(String login) {

        DefaultTableModel modelo = (DefaultTableModel) tbUsuarios.getModel();
        modelo.setNumRows(0);
        DaoUsuarios du = new DaoUsuarios();

        for (Usuarios u : du.getUsuariosByLogin(login)) {

            modelo.addRow(new Object[]{
                u.getId(),
                u.getNome(),
                u.getLogin(),
                u.getSenha()
            });

        }

    }

