    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        Usuarios u = new Usuarios();
        DaoUsuarios du = new DaoUsuarios();

        u.setNome(edNome.getText());
        u.setLogin(edLogin.getText());
        u.setSenha(edSenha.getText());
        du.salvarUsuario(u);

        edNome.setText("");
        edLogin.setText("");
        edSenha.setText("");

        readJTable();
    }//GEN-LAST:event_btSalvarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed

        if (tbUsuarios.getSelectedRow() != -1) {

            Usuarios u = new Usuarios();
            DaoUsuarios du = new DaoUsuarios();

            u.setId((int) tbUsuarios.getValueAt(tbUsuarios.getSelectedRow(), 0));

            du.excluirUsuario(u);

            edNome.setText("");
            edLogin.setText("");
            edSenha.setText("");

            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um usuario para excluir.");
        }
    }//GEN-LAST:event_btExcluirActionPerformed

    private void BtAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtAlterarActionPerformed

        if (tbUsuarios.getSelectedRow() != -1) {

            Usuarios u = new Usuarios();
            DaoUsuarios du = new DaoUsuarios();

            u.setNome(edNome.getText());
            u.setLogin(edLogin.getText());
            u.setSenha(edSenha.getText());
            u.setId((int) tbUsuarios.getValueAt(tbUsuarios.getSelectedRow(), 0));
            du.alterarUsuario(u);

            edNome.setText("");
            edLogin.setText("");
            edSenha.setText("");

            readJTable();
        }
    }//GEN-LAST:event_BtAlterarActionPerformed

